/**
 * Greatest common divisor of two integers (uses absolute values).
 * See tests/gcd.test.ts for the exact contract.
 */
export function gcd(a: number, b: number): number {
  a = Math.abs(a);
  b = Math.abs(b);
  while (b !== 0) {
    const temp = b;
    b = a % b;
    a = temp;
  }
  return a;
}
